package android.support.v8.renderscript;

public class Double4 {
    public double w;
    public double x;
    public double y;
    public double z;

    public Double4(double d, double d2, double d3, double d4) {
        this.x = d;
        this.y = d2;
        this.z = d3;
        this.w = d4;
    }
}
